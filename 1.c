#include <stdio.h>

int main(void) {
  
    int num = 10; 
    float num1 = 420.69;
    double num2 = 911.31;
    char initial = 'a';
    char str[100] = "Juan";

    printf("Integer: %d\n", num);
    printf("Float: %f\n", num1);
    printf("Double: %lf\n", num2);
    printf("Character: %c\n", initial);
    printf("String: %s\n", str);

return 0;
}